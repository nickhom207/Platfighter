Map::Map()
{
	space = TextureManager::LoadTexture("assets/black_square.png");
	block = TextureManager::LoadTexture("assets/blue_square.png");

	LoadMap(stage);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[23][40]) {
	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap() {
	int type = 0;
	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(space, src, dest);
				break;
			case 1:
				TextureManager::Draw(block, src, dest);
				break;
			default:
				break;
			}
		}
	}
}